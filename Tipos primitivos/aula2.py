'''
sep é um argumento usado apra separar os argumentos recebidos na função print() do Python.
Por exmp: print (12, 34, sep ='-') 
O resultado seria: 12-34
O padrão é um espaço em branco, mas pode ser alterado para qualquer cadeia de caracteres. 
sep significa separador. 

'''
print (12, 34, sep ='-')
print (56, 78, sep='-')
