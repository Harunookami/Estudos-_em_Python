# Tipos de int e float 
# int -> Número inteiro positivo ou negativo. int sem sinal é considerado positivo.
print (11) # int
print (-11) # int
print (0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo. 
print(1.1, 11.0) # float
print(0.0, -1.5) # float

# A função type mostra o tipo o Python inferiu ao valor. 
print( type('Rafaela'))
print( type(27))
print( type(24.10), type(-1997), type(16.21))
